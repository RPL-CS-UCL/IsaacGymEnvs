def optimize_reward(trial:optuna.Trial):


    global number

    number = trial.number
    print("OPTIMISATION TRIAL STARTS")
    print('Trial Number: ',number)


    global gaitRewardScale
    global gaitPeriodRewardScale
    global baseOrientation
    global JointAnglesRewardScale
    global basePosition
    global linearVelocityXYRewardScale
    global orientationRewardScale
    global actionRateRewardScale
    global torqueRewardScale


    gaitPeriodRewardScale = -1 * trial.suggest_float("gaitPeriodRewardScale", 1e-2, 2, log=True)
    baseOrientation = -1 * trial.suggest_float("baseOrientation", 1e-1, 4, log=True)
    JointAnglesRewardScale = -1 * trial.suggest_float("JointAnglesRewardScale", 1e-3, 1, log=True)
    basePosition = -1 * trial.suggest_float("basePosition", 1e-3, 2, log=True)
    torqueRewardScale = -1 * trial.suggest_float("torqueRewardScale", 1e-5, 1e-1, log=True)


    actionRateRewardScale = -1 * trial.suggest_float("actionRateRewardScale", 1e-2, 1e-1, log=True)
    # torqueRewardScale = -1 * trial.suggest_float("torqueRewardScale", 1e-1, 1.5, log=True)
    # linearVelocityXYRewardScale =  trial.suggest_float("linearVelocityXYRewardScale", 1e-1, 1, log=True)
    # gaitRewardScale = -1 * trial.suggest_float("gaitRewardScale", 1e-5, 1e-1, log=True)


    launch_rlg_hydra()
    reward_op = agent.reward_sim
    print("CURRENT REWARD IS ", reward_op)

    import csv
    with open('trials.csv', 'a+', newline='') as file:
        writer = csv.writer(file)

        writer.writerow([ "gaitPeriodRewardScale", "basePosition",
                         "baseOrientation", "JointAnglesRewardScale",
                         "torqueRewardScale", "actionRateRewardScale", "reward"])
        writer.writerow([gaitPeriodRewardScale, basePosition, baseOrientation,
             JointAnglesRewardScale,torqueRewardScale, actionRateRewardScale,
             reward_op])

    added_penatly = 0
    if torch.sum(agent.iteration_index) < agent.max_episode_length/1.7:
        added_penatly = 100


    return reward_op - added_penatly

