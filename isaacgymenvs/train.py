    filename = f'{cfg.train.params.config.name}.pth'  # Specify the filename
    # Check if the file exists in the directory
    if os.path.isfile(os.path.join(directory, filename)):
        new_filename = f'{cfg.train.params.config.name}_{number}.pth'  # Specify the new filename
        # Rename the file
        os.rename(os.path.join(directory, filename), os.path.join(new_directory, new_filename))
        print(f"The file '{filename}' has been renamed to '{new_filename}'.")
    else:
        print(f"The file '{filename}' does not exist in the directory.")


